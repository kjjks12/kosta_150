package phoneabstract;

public abstract class Phone {
	//필드
	public String owner;
	
	//생성자
	public Phone(){}//디폴트 생성자도 있는게 좋아!!
	
	public Phone( String owner){
		this.owner= owner;
	}
	
	//메소드
	
	public void turnOn(){
		System.out.println("폰 전원을 킵니다");
	}
	public void turnOff(){
		System.out.println("폰 전원을 끕니다");
	}	
	
}
